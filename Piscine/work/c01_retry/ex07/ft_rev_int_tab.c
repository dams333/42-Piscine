void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	tmp;

	i = -1;
	while (++i < size / 2)
	{
		j = size - i - 1;
		tmp = tab[i];
		tab[i] = tab[j];
		tab[j] = tmp;
	}
}
